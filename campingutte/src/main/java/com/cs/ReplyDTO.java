package com.cs;

public class ReplyDTO {
	private int compNo;
	private String memberId;
	private String memberName;
	private String compReplyContent;
	private String compReplyDate;
	
	
	public int getCompNo() {
		return compNo;
	}
	public void setCompNo(int compNo) {
		this.compNo = compNo;
	}
	public String getMemberId() {
		return memberId;
	}
	public void setMemberId(String memberId) {
		this.memberId = memberId;
	}
	public String getMemberName() {
		return memberName;
	}
	public void setMemberName(String memberName) {
		this.memberName = memberName;
	}
	public String getCompReplyContent() {
		return compReplyContent;
	}
	public void setCompReplyContent(String compReplyContent) {
		this.compReplyContent = compReplyContent;
	}
	public String getCompReplyDate() {
		return compReplyDate;
	}
	public void setCompReplyDate(String compReplyDate) {
		this.compReplyDate = compReplyDate;
	}
	
	
	
  
}
